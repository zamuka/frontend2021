// ввод данных пользователем
const daysTillWeekend = Number(prompt('Сколько дней до выходных?'));

let sighMessage = 'O';

for (let i = 0; i < daysTillWeekend; i++) {
    sighMessage = sighMessage + '-xo';
}
if ( daysTillWeekend == 0 ) {
    sighMessage = sighMessage + '!';
}
